# 2. O‘zgaruvchilar va print
# Python’da o‘zgaruvchi (variable) yaratish va ekranga chiqarish

ism = "Alisher"  # O‘zgaruvchi yaratish
yosh = 20        # Ikkinchi o‘zgaruvchi

print(ism)       # Ekranga ismni chiqaradi
print(yosh)      # Ekranga yoshni chiqaradi
print(f"{ism} {yosh} yoshda.")  # Formatlangan chiqarish
